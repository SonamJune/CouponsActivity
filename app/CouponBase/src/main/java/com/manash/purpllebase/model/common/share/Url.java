package com.manash.purpllebase.model.common.share;

public class Url {
    private String fbShareUrl;
    private String whatsappShareUrl;
    private String twitterShareUrl;
    private String gplusShareUrl;

    public String getFbShareUrl() {
        return fbShareUrl;
    }

    public void setFbShareUrl(String fbShareUrl) {
        this.fbShareUrl = fbShareUrl;
    }

    public String getWhatsappShareUrl() {
        return whatsappShareUrl;
    }

    public void setWhatsappShareUrl(String whatsappShareUrl) {
        this.whatsappShareUrl = whatsappShareUrl;
    }

    public String getTwitterShareUrl() {
        return twitterShareUrl;
    }

    public void setTwitterShareUrl(String twitterShareUrl) {
        this.twitterShareUrl = twitterShareUrl;
    }

    public String getGplusShareUrl() {
        return gplusShareUrl;
    }

    public void setGplusShareUrl(String gplusShareUrl) {
        this.gplusShareUrl = gplusShareUrl;
    }
}
